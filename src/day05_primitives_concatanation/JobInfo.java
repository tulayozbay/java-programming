package day05_primitives_concatanation;

public class JobInfo {
    public static void main(String[] args){
        String title="Java SDET";
        String company="Palo Alto Networks";
        String jobDescription="Experience in Java, Selenium, Cucumber, Tomato, JUnit";
        double salary=123000;
        byte yearsOfExp=2;
        boolean hasBenefits=true;
        System.out.println("Title          :\t "+title);
        System.out.println("Company        :\t "+company);
        System.out.println("Job description:\t "+jobDescription);
        System.out.println("Salary         :\t $"+salary);
        System.out.println("Experience     :\t "+yearsOfExp);
        System.out.println("Has benefits?  :\t "+hasBenefits);


    }
}
